def sockMerchant(n, ar):
    # Write your code here
    temp=[]
    count=0
    while ar:
        i=ar[0]
        if i not in temp:
            temp.append(i)
        elif i in temp:
            temp.remove(i)
            count+=1
        ar.remove(i)
    return count
            
        

